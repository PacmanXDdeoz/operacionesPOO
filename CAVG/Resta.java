class Resta {
    int resta1;
    int resta2;
    Resta() {
    }
    public int getResta1() {
        return resta1;
    }
    public void setResta1(int resta1) {
        this.resta1 = resta1;
    }
    public int getResta2() {
        return resta2;
    }
    public void setResta2(int resta2) {
        this.resta2 = resta2;
    }
    @Override
    public String toString() {
        return "Resta [resta1=" + resta1 + ", resta2=" + resta2 + "]";
    }
}