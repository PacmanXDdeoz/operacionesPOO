class Suma {
    int sum1;
    int sum2;
    Suma() {
    }
    public int getSum1() {
        return sum1;
    }
    public void setSum1(int sum1) {
        this.sum1 = sum1;
    }
    public int getSum2() {
        return sum2;
    }
    public void setSum2(int sum2) {
        this.sum2 = sum2;
    }
    @Override
    public String toString() {
        return "Suma [sum1=" + sum1 + ", sum2=" + sum2 + "]";
    }
}